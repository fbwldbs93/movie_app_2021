/*
    -------------- 어떻게 create-react-app을 실행하는가? -----------
    터미널에서 npm start 입력
    :: compile 되어서 이제 movie app을 브라우저에서 볼 수 있고
    두개의 URL 을 줌.

    http://localhost:3000 우리 컴퓨터에 있고
    http://172.30.1.50:3000 이건 와이파이에 있을 거임.
    - 와이파이에 친구가 있거나 폰에 테스트 하고 싶을때
    이 URL을 사용.

    끝! 이게 다임. So simple
*/

/*
    ------------ 첫 프로젝트 git에 저장하기! -----------

    1. 작업폴더 경로에서 git init 입력
    - 기존 저장소를 다시 초기화하라는 메세지.
    또는 저장소를 초기화 하라고 뜸. (둘 다 괜찮음)

    2. git remote add origin 깃 리포지터리 주소
    - 깃허브에서 생성한 리포지터리 경로 등록 

    3. git add .
    - 작업중인 모든 파일 등록

    4. git commit -m "커밋 이름"

    5. git push origin master
    
*/